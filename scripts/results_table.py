#!/usr/bin/env python3
"""Aggregate all eval JSONs under experiments/ into a structured results table.

Reads every ``experiments/**/eval*.json`` written by ``trinity.eval``, and emits:
  - a per-eval Markdown table (TRINITY vs best single vs random, per coordinator),
  - a multi-task summary (TRINITY per-task-best avg vs best fixed single model avg),
  - the R1/R2/R4 invariant verdicts.

Run on the box where experiments/ lives:
    python scripts/results_table.py            # Markdown to stdout
    python scripts/results_table.py --json      # also dump consolidated results.json
"""
from __future__ import annotations

import argparse
import glob
import json
from pathlib import Path


def load_rows(root: str = "experiments") -> list[dict]:
    rows = []
    for p in sorted(glob.glob(f"{root}/**/eval*.json", recursive=True)):
        try:
            d = json.load(open(p))
        except Exception:
            continue
        r = d.get("results", {})
        singles = {k.split("::", 1)[1]: v for k, v in r.items() if k.startswith("single::")}
        if not singles or "TRINITY" not in r:
            continue
        # best_model over the NON-NULL single scores only: a partial/older eval*.json can
        # carry `"single::x": null`, and `max(singles, key=singles.get)` crashes comparing
        # None to a float. The multi-task summary (`single_avg`) already filters nulls, so
        # keep the full `singles` map and just guard the per-row best here.
        numeric = {m: v for m, v in singles.items()
                   if isinstance(v, (int, float)) and not isinstance(v, bool)}
        best_model = max(numeric, key=lambda m: numeric[m]) if numeric else None
        rows.append({
            "file": p,
            "benchmark": d.get("benchmark", "?"),
            "coordinator": Path(p).parent.name,
            "variant": Path(p).stem,  # eval / eval_fixed / *_rigorous
            "trinity": r.get("TRINITY"),
            "random": r.get("random_routing"),
            "best_single": numeric[best_model] if best_model is not None else None,
            "best_model": best_model,
            "singles": singles,
        })
    return rows


def fmt(x) -> str:
    return f"{x:.3f}" if isinstance(x, (int, float)) else "—"


def _bench_best(rows: list[dict], key: str) -> float:
    """Best (max) non-null value of ``key`` across a benchmark's rows, else 0.0.

    ``load_rows`` only guarantees the ``TRINITY`` / ``single::`` *keys* are present,
    not that every value is non-null — an older or partially-written ``eval*.json``
    can carry ``"TRINITY": null`` (or lack ``random_routing``). Skipping ``None``
    here (mirroring the per-row table's ``x or 0``) keeps the multi-task summary
    from crashing on ``max``/``sum`` over a ``None``.
    """
    vals = [r[key] for r in rows if r.get(key) is not None]
    return max(vals) if vals else 0.0


def render(rows: list[dict]) -> str:
    out = ["# TRINITY replication — results\n"]
    out.append("Auto-generated by `scripts/results_table.py` from `experiments/**/eval*.json`.\n")

    out.append("## Per-coordinator held-out evals\n")
    out.append("| benchmark | coordinator | variant | TRINITY | best single (model) | random | TRINITY>best? | TRINITY>random? |")
    out.append("|---|---|---|---|---|---|---|---|")
    for r in rows:
        gt_best = "✅" if (r["trinity"] or 0) > (r["best_single"] or 0) else ("≈" if abs((r["trinity"] or 0) - (r["best_single"] or 0)) <= 0.03 else "❌")
        gt_rand = "✅" if (r["trinity"] or 0) > (r["random"] or 0) else "❌"
        out.append(f"| {r['benchmark']} | {r['coordinator']} | {r['variant']} | "
                   f"{fmt(r['trinity'])} | {fmt(r['best_single'])} ({r['best_model'] or '—'}) | "
                   f"{fmt(r['random'])} | {gt_best} | {gt_rand} |")

    # Multi-task summary: best TRINITY per benchmark, and best fixed single model averaged.
    by_bench: dict[str, list[dict]] = {}
    for r in rows:
        by_bench.setdefault(r["benchmark"], []).append(r)
    benches = sorted(by_bench)
    if len(benches) >= 2:
        out.append("\n## Multi-task summary (the paper's R1/R2 claim)\n")
        # union of single models across benches
        models = sorted({m for r in rows for m in r["singles"]})
        # Every system is reduced across a benchmark's eval rows the SAME way (per-bench best),
        # then averaged over benchmarks. Reducing TRINITY with max while averaging the baselines
        # would compare a cherry-picked best against a mean and bias R1/R2 toward TRINITY.
        # best per-bench single score for each model (max across that bench's evals)
        n_benches = len(benches)

        def single_avg(model: str) -> tuple[float | None, int]:
            """Per-bench best for ``model`` averaged over the benches it covers.

            Returns ``(average, n_covered)``. A single model need not appear on
            every benchmark; we average only over the benches it *does* cover and
            report how many that was, so the caller can tell a full-coverage fixed
            baseline from a partial one. A partial model's average is taken over its
            own — often favorable — subset of tasks and is NOT comparable to
            TRINITY's all-benchmark average, so it is excluded from the R1/R2
            comparator below.
            """
            vals = []
            for b in benches:
                bench_vals = [r["singles"].get(model) for r in by_bench[b] if model in r["singles"]]
                bench_vals = [v for v in bench_vals if v is not None]
                if bench_vals:
                    vals.append(max(bench_vals))
            if not vals:
                return None, 0
            return sum(vals) / len(vals), len(vals)
        # TRINITY per-task best (max TRINITY across coordinators per bench), averaged
        trin_avg = sum(_bench_best(by_bench[b], "trinity") for b in benches) / n_benches
        # Random routing: per-bench best too, so R4 also compares like with like.
        rand_avg = sum(_bench_best(by_bench[b], "random") for b in benches) / n_benches
        out.append(f"Averaged across {benches}:\n")
        out.append("| system | multi-task average |")
        out.append("|---|---|")
        out.append(f"| **TRINITY (per-task best coordinator)** | **{trin_avg:.3f}** |")
        # Only a FULL-coverage single model is a valid "best fixed single" comparator
        # for R1/R2 (SPEC R2: TRINITY > every single model on every task): a model
        # missing from some benchmarks has no score on those tasks, so its subset-
        # average — divided by its own coverage, not all benches — cannot be the best
        # fixed single across every task and would spuriously flip the verdict.
        full_cov_avgs = []
        for m in models:
            sa, n_cov = single_avg(m)
            if sa is None:
                continue
            if n_cov == n_benches:
                out.append(f"| single: {m} (fixed) | {sa:.3f} |")
                full_cov_avgs.append(sa)
            else:
                out.append(f"| single: {m} (partial: {n_cov}/{n_benches} benches) | {sa:.3f} |")
        out.append(f"| random routing | {rand_avg:.3f} |")
        if full_cov_avgs:
            best_fixed = max(full_cov_avgs)
            out.append(f"\n**R1/R2** (TRINITY avg > best fixed single avg): "
                       f"{'✅ HOLDS' if trin_avg > best_fixed else '❌'} "
                       f"({trin_avg:.3f} vs {best_fixed:.3f})")
        else:
            out.append("\n**R1/R2** (TRINITY avg > best fixed single avg): "
                       f"N/A — no single model covers all {n_benches} benchmarks "
                       f"({', '.join(benches)}), so there is no valid fixed-single comparator")
        out.append(f"**R4** (TRINITY avg > random avg): "
                   f"{'✅ HOLDS' if trin_avg > rand_avg else '❌'} ({trin_avg:.3f} vs {rand_avg:.3f})")
    return "\n".join(out) + "\n"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--root", default="experiments")
    ap.add_argument("--json", action="store_true", help="also write results.json")
    args = ap.parse_args()
    rows = load_rows(args.root)
    print(render(rows))
    if args.json:
        # Write alongside the eval*.json it consolidates, honoring --root (the old
        # hardcoded "experiments/results.json" ignored --root and crashed when the
        # chosen root was not the default "experiments").
        Path(args.root, "results.json").write_text(json.dumps(rows, indent=2))


if __name__ == "__main__":
    main()
